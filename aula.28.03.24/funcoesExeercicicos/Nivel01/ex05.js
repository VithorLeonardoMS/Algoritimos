//Conversão de Celsius para Fahrenheit: Crie uma função que receba uma temperatura em Celsius e retorne o equivalente em Fahrenheit. Você deve usar return.
let convercao = function(num1){
    return (num1 * 9/5) + 32

}
console.log(`A converção de 23c° para Fahrenheit reslta em ${convercao(23)}`)